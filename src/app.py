"""
High School Management System API

A super simple FastAPI application that allows students to view and sign up
for extracurricular activities at Mergington High School.
"""

from fastapi import FastAPI, HTTPException, Depends, status
from fastapi.security import HTTPBasic, HTTPBasicCredentials
import secrets
from fastapi.staticfiles import StaticFiles
from fastapi.responses import RedirectResponse
import os
from pathlib import Path

app = FastAPI(title="Mergington High School API",
              description="API for viewing and signing up for extracurricular activities with roles and authentication")

# Seguridad básica HTTP
security = HTTPBasic()

# Usuarios y roles en memoria
users_db = {
    "admin1": {"username": "admin1", "password": "adminpass", "role": "admin"},
    "inst1": {"username": "inst1", "password": "instpass", "role": "institucion"},
    "student1": {"username": "student1", "password": "studpass", "role": "estudiante"},
    "parent1": {"username": "parent1", "password": "parentpass", "role": "padre"}
}

# Función para obtener usuario autenticado
def get_current_user(credentials: HTTPBasicCredentials = Depends(security)):
    user = users_db.get(credentials.username)
    if not user or not secrets.compare_digest(credentials.password, user["password"]):
        raise HTTPException(
            status_code=status.HTTP_401_UNAUTHORIZED,
            detail="Credenciales inválidas",
            headers={"WWW-Authenticate": "Basic"},
        )
    return user

# Mount the static files directory
current_dir = Path(__file__).parent
app.mount("/static", StaticFiles(directory=os.path.join(Path(__file__).parent,
          "static")), name="static")

