@app.get("/")
def root():
    return RedirectResponse(url="/static/index.html")


@app.get("/activities")
def get_activities(user: dict = Depends(get_current_user)):
    """Solo usuarios autenticados pueden ver actividades"""
    return activities


@app.post("/activities/{activity_name}/signup")
def signup_for_activity(activity_name: str, email: str, user: dict = Depends(get_current_user)):
    """Solo admins e instituciones pueden registrar estudiantes"""
    if user["role"] not in ["admin", "institucion"]:
        raise HTTPException(status_code=403, detail="No tienes permisos para registrar estudiantes")
    # Validar actividad
    if activity_name not in activities:
        raise HTTPException(status_code=404, detail="Activity not found")
    activity = activities[activity_name]
    if email in activity["participants"]:
        raise HTTPException(status_code=400, detail="Student is already signed up")
    activity["participants"].append(email)
    return {"message": f"Signed up {email} for {activity_name}"}


@app.delete("/activities/{activity_name}/unregister")
def unregister_from_activity(activity_name: str, email: str, user: dict = Depends(get_current_user)):
    """Solo admins e instituciones pueden desregistrar estudiantes"""
    if user["role"] not in ["admin", "institucion"]:
        raise HTTPException(status_code=403, detail="No tienes permisos para desregistrar estudiantes")
    if activity_name not in activities:
        raise HTTPException(status_code=404, detail="Activity not found")
    activity = activities[activity_name]
    if email not in activity["participants"]:
        raise HTTPException(status_code=400, detail="Student is not signed up for this activity")
    activity["participants"].remove(email)
    return {"message": f"Unregistered {email} from {activity_name}"}
